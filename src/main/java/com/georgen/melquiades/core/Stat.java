package com.georgen.melquiades.core;

public class Stat {
    private double avg;
    private double p50;
    private double p80;
    private double p90;
    private double p95;
    private double p98;

    public double getAvg() { return avg; }

    public void setAvg(double avg) { this.avg = avg; }

    public double getP50() { return p50; }

    public void setP50(double p50) { this.p50 = p50; }

    public double getP80() { return p80; }

    public void setP80(double p80) { this.p80 = p80; }

    public double getP90() { return p90; }

    public void setP90(double p90) { this.p90 = p90; }

    public double getP95() { return p95; }

    public void setP95(double p95) { this.p95 = p95; }

    public double getP98() { return p98; }

    public void setP98(double p98) { this.p98 = p98; }
}
